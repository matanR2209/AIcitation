/**
 * Extracts source names from the "Sources:" block the AI returns.
 * Does NOT generate or trust LLM-provided URLs (they hallucinate).
 * If any real URLs slip through we validate them via HTTP HEAD.
 */
function extractSources(text) {
  const results = [];
  const seenNames = new Set();

  // ── Parse the structured Sources section ──────────────────────────────────
  const sectionMatch = text.match(/(?:^|\n)(?:#+\s*)?sources?:?\s*\n([\s\S]*?)(?=\n(?:#+\s*)\S|$)/i);
  const block = sectionMatch ? sectionMatch[1] : text;

  // Match lines like: "1. ESPN" or "- Basketball Reference" or "* NBA.com"
  // Also handles "1. ESPN: https://..." — we keep the name, drop the URL
  const lineRe = /^[\s\-*\d.)]*([A-Za-z0-9][^\n:]{2,60})(?::.*)?$/gm;
  let m;
  while ((m = lineRe.exec(block)) !== null) {
    const name = m[1].trim().replace(/\*+/g, '').replace(/\.$/, '');
    if (!name || seenNames.has(name.toLowerCase())) continue;

    // Skip lines that are clearly prose, not source names
    if (name.split(' ').length > 6) continue;

    seenNames.add(name.toLowerCase());
    results.push({ name });
  }

  // ── Also match via KNOWN_SOURCES patterns for reliability ─────────────────
  const lower = text.toLowerCase();
  KNOWN_SOURCES.forEach(src => {
    if (seenNames.has(src.canonical.toLowerCase())) return;
    if (src.patterns.some(p => lower.includes(p.toLowerCase()))) {
      seenNames.add(src.canonical.toLowerCase());
      results.push({ name: src.canonical });
    }
  });

  return results;
}

