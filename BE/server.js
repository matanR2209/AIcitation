// Write disclaimer header once when the log file is first created
function writeLogHeader(enginesInUse) {
  const flagged = enginesInUse.filter(e => HALLUCINATION_ENGINES.includes(e));
  if (!flagged.length) return;
  const names = flagged.map(e => e.charAt(0).toUpperCase() + e.slice(1)).join(', ');
  const header = [
    '='.repeat(72),
    `Sports AI Citation Tracker — Session: ${SESSION_TS}`,
    '='.repeat(72),
    '',
    `Disclaimer: Results from ${names} are generated by language models`,
    'that do not have live web access. Source names cited are generally',
    'reliable, but any specific URLs produced by these engines may be',
    'hallucinated (correct format, fabricated article IDs). To verify a',
    "claim, search the source name + topic directly on the publisher's site.",
    '',
    'Perplexity results (when added) include real, web-searched URLs.',
    '',
    '='.repeat(72),
    '',
  ];
  fs.writeFileSync(SESSION_LOG, header.join('\n') + '\n');
}

