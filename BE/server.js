function writeLog(promptIdx, promptText, ai, text) {
  const sources = extractSources(text);
  const lines   = [
    `prompt ${promptIdx + 1} [${promptText.slice(0, 80)}${promptText.length > 80 ? '…' : ''}], ${ai} results:`,
  ];

  if (sources.length === 0) {
    lines.push('  (no sources detected)');
  } else {
    sources.forEach(({ name }) => lines.push(`  - ${name}`));
  }
  lines.push('');

  fs.appendFileSync(SESSION_LOG, lines.join('\n') + '\n');
  console.log(`[LOG] wrote ${sources.length} source(s) for prompt ${promptIdx + 1} / ${ai}`);
}

// ─── WebSocket ───────────────────────────────────────────────────────────────

function broadcast(msg) {
  const data = JSON.stringify(msg);
  wss.clients.forEach(client => {
    if (client.readyState === 1) client.send(data);
  });
}

wss.on('connection', ws => {
  console.log('[WS] client connected');
  ws.on('close', () => console.log('[WS] client disconnected'));
});

// ─── Routes ──────────────────────────────────────────────────────────────────

let activeRunner = null;

// POST /api/auth — validates the magic password
app.post('/api/auth', (req, res) => {
  const { password } = req.body;
  const magic = process.env.MAGIC_PASSWORD;
  if (!magic) return res.status(500).json({ ok: false, error: 'MAGIC_PASSWORD not set in .env' });
  if (password === magic) return res.json({ ok: true });
  return res.status(401).json({ ok: false, error: 'Invalid password' });
});

// POST /api/run-prompt
app.post('/api/run-prompt', (req, res) => {
  const { promptIdx, promptText, engines, keys } = req.body;
  if (promptIdx === undefined || !promptText) {
    return res.status(400).json({ ok: false, error: 'Missing promptIdx or promptText' });
  }
  if (activeRunner) {
    return res.status(409).json({ ok: false, error: 'A run is already in progress' });
  }

  const engineList = Array.isArray(engines) && engines.length ? engines : null;
  console.log(`[BE] Running prompt ${promptIdx} [${engineList ? engineList.join(',') : 'all'}]: "${promptText.slice(0, 60)}..."`);

  // Write disclaimer header once per session on the first run
  if (!fs.existsSync(SESSION_LOG)) {
    writeLogHeader(['claude', 'chatgpt', 'gemini']);
  }

  const envExtra = { PROMPT_IDX: String(promptIdx), PROMPT_TEXT: promptText };
  if (engineList) envExtra.ENGINES = engineList.join(',');
  // Allow user-supplied API keys to override .env values
  if (keys) {
    if (keys.anthropic) envExtra.ANTHROPIC_API_KEY = keys.anthropic;
    if (keys.gemini)    envExtra.GEMINI_API_KEY    = keys.gemini;
    if (keys.tavily)    envExtra.TAVILY_API_KEY     = keys.tavily;
    if (keys.github)    envExtra.GITHUB_TOKEN       = keys.github;
  }

  const runner = spawn('node', [path.join(__dirname, 'runner.js')], {
    env:   { ...process.env, ...envExtra },
    stdio: ['ignore', 'pipe', 'pipe'],
  });

  activeRunner = runner;

  runner.stdout.on('data', chunk => {
    chunk.toString().split('\n').forEach(line => {
      line = line.trim();
      if (!line) return;
      if (line.startsWith('[MSG]')) {
        try {
          const msg = JSON.parse(line.slice(5).trim());
          // Hook: write log when a response comes in
          if (msg.type === 'response') {
            writeLog(msg.promptIdx, promptText, msg.ai, msg.text);
          }
          broadcast(msg);
        } catch (e) {
          console.error('[BE] bad MSG line:', line);
        }
      } else {
        console.log('[runner]', line);
      }
    });
  });

  runner.stderr.on('data', chunk => {
    console.error('[runner stderr]', chunk.toString().trim());
  });

  runner.on('close', code => {
    console.log(`[BE] runner exited with code ${code}`);
    activeRunner = null;
    broadcast({ type: 'done' });
  });

  res.json({ ok: true });
});

// GET /api/status
app.get('/api/status', (req, res) => {
  res.json({ running: !!activeRunner });
});

// POST /api/stop
app.post('/api/stop', (req, res) => {
  if (activeRunner) {
    activeRunner.kill();
    activeRunner = null;
    broadcast({ type: 'stopped' });
  }
  res.json({ ok: true });
});

server.listen(PORT, () => {
  console.log(`[BE] ready on :${PORT}`);
  console.log(`[LOG] session log: ${SESSION_LOG}`);
});
